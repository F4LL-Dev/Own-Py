print("--------------------even Numbers 1-10------------")
i = 1;
while i <= 10:
    if i % 2 == 0:
        print(i)
    i += 1
print("\n Loop Terminated at i= ", i)

print("--------------------odd Numbers 1-10------------")
i = 1;
while i <= 10:
    if i % 2 != 0:
        print(i)
    i += 1
print("\n Loop Terminated at i= ", i)

