print(" Printing each element from a string using for ")
for x in "F4LL":
    print(x)

print("Printing the list elements using for")
list1 = {"1st element", "2nd element", "3rd Element"}
for x in list1:
    print(x)

print("Printing the values from rangefunction range(5)  ")
for x in range(5):
    print(x)

print("Using Index printing using for")
for x in range(len(list1)):
    print(list1[x])
